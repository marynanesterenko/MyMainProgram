package com.java.class03;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int num1 = 1;
        int num2 = 2;

        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(10 % 3); // answer is "-1"


    }
}
