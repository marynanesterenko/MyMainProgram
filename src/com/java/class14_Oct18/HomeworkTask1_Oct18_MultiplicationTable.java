package com.java.class14_Oct18;

public class HomeworkTask1_Oct18_MultiplicationTable {
    // 1. Write a program to print a multiplication table of 5
    public static void main(String[] args) {

        int i = 1;
        while ( i <= 10) {
            System.out.println( i + " * " + "5" + " = " + (i * 5));
            i++;
        }
    }
}
