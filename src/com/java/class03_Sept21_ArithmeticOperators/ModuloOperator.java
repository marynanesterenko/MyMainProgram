package com.java.class03_Sept21_ArithmeticOperators;

public class ModuloOperator {
    public static void main(String[] args) {
        double a = 0.3;
        double b = 0.12;
        double c = a % b;
        System.out.println(c);

        double a1 = 0.12;
        double b1 = 0.3;
        double c1 = a1 / b1;
        System.out.println(c1);
    }
}
