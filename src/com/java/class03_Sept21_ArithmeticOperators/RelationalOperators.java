package com.java.class03_Sept21_ArithmeticOperators;

public class RelationalOperators {
    public static void main(String[] args) {
        int a = 10, b = 11;
        System.out.println(a > b); // false
        System.out.println(a < b); // true
        System.out.println(a >= b); // false
        System.out.println(a <= b); // true
        System.out.println(a != b); // true
        System.out.println(a == b); // false
    }
}
